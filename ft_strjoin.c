#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;

	str = ft_strnew(ft_strlen(s1) + ft_strlen(s2) + 1);
	ft_strcat(str, (char*)s1);
	ft_strcat(str, (char*)s2);
	return (str);
}
